import math

x = float(input("Enter the value of X:\t"))
print("log 10 (x) =", math.log10(x))
print("log e (e) =", math.log(math.e))
print("log 5 (625) =", math.log(625, 5))

print("sin (x) =", math.sin(x))
print("cos (x) =", math.cos(x))
print("tan (x) =", math.tan(x))
print("atan (x) =", math.atan(x))


angle = float(input("Enter the angle in radians:\t"))

print("Sin", angle, "=", math.sin(angle))
print("Cos", angle, "=", math.cos(angle))
print("Tan", angle, "=", math.tan(angle))
print("Atan", angle, "=", math.atan(angle))


